#include <unistd.h>

int main(int argc, char **str)
{
	int	i;

	i = 0;
	if (argc == 2)
	{
		while (str[1][i] == 32 || str[1][i] == 9)
			i++;
		while (str[1][i] != 32 && str[1][i] != 9 && str[1][i])
		{
			write(1, &str[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);	
}
