#include <unistd.h>

int main(int ac, char **av)
{
	int i = -1;
	int j = -1;

	if (ac == 3)
	{
		while (av[1][++i])
			while (av[2][++j] && (av[1][i] != av[2][j]));
		if (av[2][j] == '\0')
		{
			write(1, "0", 1);
			return (0);
		}
		write(1, "1", 1);
	}
	write(1, "\n", 1);
	return (0);
}

